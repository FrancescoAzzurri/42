#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((*s1 != '\0' || *s2 != '\0') && i < n)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
			i++;
		}
		else
		{
			return (*s1 - *s2);
		}
	}
	if ((*s1 == '\0' || *s2 == '\0') && i < n)
	{
		return (*s1 - *s2);
	}
	return (0);
}
